package loop;

public class while1_1 {
    public static void main(String[] args) {
        // while 반복문
        // 예시, 단순 또는 증감연산 사용해도 상관은 없지만 너무 많으면 반복문 사용
        int count = 0;

        count = count + 1;
        System.out.println("현재 숫자는: " + count); // 1
        count = count + 1;
        System.out.println("현재 숫자는: " + count); // 2
        count = count + 1;
        System.out.println("현재 숫자는: " + count); // 3

    }
}
