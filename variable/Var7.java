package variable;

public class Var7 {
    public static void main(String[] args) {
//        리터럴 -> = 뒤에 개발자가 직접 쓴 고정값
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; //불리언(boolean) true(참), false(거짓) 만 입력가능
        char d = 'A'; //그냥 문자 하나! -> "이 아닌 ' 이거써야됨
        String e = "Hello Java"; //문자열! 문자열을 다루기 위한 특별한 타입! 이때 -> " 이걸로만 쓰기!

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

//        이 중에서 int,double,boolean,String 만쓴다
    }
}
